/**
 * Get a translation string for the given language and key.
 * Falls back to Hindi, then English if key is missing.
 * @param {string} lang - Language code
 * @param {string} key - Translation key (supports dot notation e.g. 'pages.home')
 * @returns {*} Translated string or function
 */
export function t(lang, key) {
  const keys = key.split('.')
  const resolve = (obj) => keys.reduce((acc, k) => acc?.[k], obj)
  return (
    resolve(TRANSLATIONS[lang]) ??
    resolve(TRANSLATIONS['hi']) ??
    resolve(TRANSLATIONS['en']) ??
    key
  )
}

/**
 * Returns the full translations object for a given language.
 * Falls back to Hindi if not found.
 */
export function getTranslations(lang) {
  return TRANSLATIONS[lang] || TRANSLATIONS['hi']
}

export function getUiLanguage(languageCode, uiLanguageMap) {
  return uiLanguageMap[languageCode] || languageCode || 'hi'
}

